def door(door_tag):
    room = getattr(sys.modules[__name__], ("room_" + door_tag))

    boundary.xview_moveto(0)
    boundary.yview_moveto(0)

    boundary.xview_scroll(int(room.start[0]/2)*48, UNITS)
    boundary.yview_scroll(int(room.start[1]/2)*48, UNITS)

    y_diff = room.height - loc.room.height

    for _ in range(0, y_diff):
        boundary.move(player, 0, 24)

    for _ in range(y_diff, 0):
        boundary.move(player, 0, -24)

    for Y_level in room.Y_list:
        for tile in Y_level:
            tile.update = True

    return room


