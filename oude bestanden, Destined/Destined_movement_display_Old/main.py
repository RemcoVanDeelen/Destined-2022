class Tile:

    def __init__(self, x, y, wall, sprite, interaction=None, activation=None, base_x=2,
                 base_y=2):
        self.x = x
        self.y = y
        self.wall = wall
        self.sprite = sprite
        self.show_sprite = "ERROR"
        self.interaction = interaction
        self.activation = activation
        self.base_x = base_x
        self.base_y = base_y
        self.update = True
        self.widget = None

    def show(self, x, y):
        if self.widget is not None:
            # self.widget.destroy()
            boundary.itemconfigure(self.widget, image=self.show_sprite)

        else:
            # widget = Label(boundary, image=self.show_sprite, borderwidth=0)
            # widget.grid(row=y, column=x)
            boundary.create_image(x * 48, y * 48, image=self.show_sprite, tag=("x" + str(x) + "y" + str(y)))
            self.widget = "x" + str(x) + "y" + str(y)


