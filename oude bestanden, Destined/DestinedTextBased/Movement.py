class Coordinates:

    def __init__(self):
        self.x = 2
        self.y = 2
        self.coords = [self.x, self.y]
        self.room = room_P1

        num = round(self.coords[1] - 1)
        tile_list = self.room.Y_list[num.__int__()]
        num = round(self.coords[0] - 1)
        tile = tile_list[num.__int__()]
        tile.player = True

    def move(self, direction, run=False):
        if run is True:
            speed = 1.5
        else:
            speed = 1

        if direction == "North":
            self.y += speed

        elif direction == "South":
            self.y -= speed

        elif direction == "East":
            self.x += speed

        elif direction == "West":
            self.x -= speed

        elif direction == "NorthEast":
            self.x += (speed**2) / 2
            self.y += (speed**2) / 2

        elif direction == "NorthWest":
            self.x -= (speed**2) / 2
            self.y += (speed**2) / 2

        elif direction == "SouthEast":
            self.x += (speed**2) / 2
            self.y -= (speed**2) / 2

        elif direction == "SouthWest":
            self.x -= (speed**2) / 2
            self.y -= (speed**2) / 2

        num = round(self.coords[1] - 1)
        tile_list = self.room.Y_list[num.__int__()]
        num = round(self.coords[0] - 1)
        tile = tile_list[num.__int__()]
        tile.player = False

        num = round(self.y - 1)
        tile_list = self.room.Y_list[num.__int__()]
        num = round(self.x - 1)
        tile = tile_list[num.__int__()]

        # Interactions
        if tile.activation is not None:
            if tile.activation == "Enter door":
                self.x = tile.base_x
                self.y = tile.base_y
                print(tile, tile.activation, tile.interaction)
                print(tile.interaction())
                self.room = tile.interaction()
            else:
                tile.interaction()

        if tile.wall is True:
            self.x = self.coords[0]
            self.y = self.coords[1]

        num = round(self.y - 1)
        tile_list = self.room.Y_list[num.__int__()]
        num = round(self.x - 1)
        tile = tile_list[num.__int__()]
        tile.player = True

        self.coords = [self.x, self.y]
        self.show_tiles()

    def show_tiles(self):
        list_num = 17
        tile_num = 0
        tile_screen = "\n"

        for _ in range(0, 576):

            tile_list = self.room.Y_list[list_num]
            tile = tile_list[tile_num]
            if tile.player is True:
                tile.show_sprite = tile.player_sprite
            else:
                tile.show_sprite = tile.sprite

            if tile_num == 31:
                tile_num = 0
                list_num -= 1
                tile_screen += tile.show_sprite + "\n"
            else:
                tile_num += 1
                tile_screen += tile.show_sprite
        print(tile_screen)


